def render(model: Any = None) -> None:
    hero(
        title="Частые вопросы",
        subtitle=(
            "Ответы на самые распространённые вопросы о сахарном диабете, "
            "его профилактике и о том, как работает этот портал."
        ),
        badge="❓ FAQ",
    )

    section_header(
        "Не нашли ответ?",
        "Если вашего вопроса нет в списке — обратитесь к врачу или к проверенным "
        "источникам: ВОЗ, Международная диабетическая федерация (IDF), Минздрав России.",
    )

    for idx, (question, answer) in enumerate(_FAQ):
        with st.expander(f"**{question}**", expanded=(idx == 0)):
            st.markdown(answer)

    disclaimer(
        "Информация в этом разделе носит образовательный характер и не заменяет "
        "консультацию специалиста."
    )

    col1, col2, _ = st.columns([1, 1, 2])
    with col1:
        if st.button("Пройти оценку →", key="faq_to_assess"):
            st.session_state.current_page = "🩺 Оценка риска"
            st.rerun()
    with col2:
        if st.button("Подробнее о диабете →", key="faq_to_edu"):
            st.session_state.current_page = "📚 О диабете"
            st.rerun()
